# ==========================================================
# ==========================================================
# MAIN
# ==========================================================

def main():

    sample_image = Path(
        r"D:/PLACEMENT/HYPERVERGE/PIXEL_RESTORE/data/processed/gaussian_noise/00040534.png"
    )

    recommendation = {

        "recommended_pipeline": [

            {
                "step": 1,
                "operation": "Denoise",
                "model": "DnCNN"
            },

            {
                "step": 2,
                "operation": "Contrast Enhancement",
                "model": "CLAHE"
            }

        ]

    }

    restore_image(
        sample_image,
        recommendation
    )


if __name__ == "__main__":

    main()